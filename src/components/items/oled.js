class OLED extends React.Component {
    constructor(props) {
        console.log('constructor called')
        super(props);
        this.drawQueue = [];
        this.pixels = new Array(XX * YY);
        this.pixels.fill(false);
    }

    _fillRect = (x, y, w, h) => {
        for (let i = x; i < x + w; i++) {
            for (let j = y; j < y + h; j++) {
                this.pixels[j * XX + i] = true;
            }
        }
    }

    _clearRect = (x, y, w, h) => {
        for (let i = x; i < x + w; i++) {
            for (let j = y; j < y + h; j++) {
                this.pixels[j * XX + i] = false;
            }
        }
    }

    draw = (x, y, x0, y0, bmp, external = true) => {
        var w = x0 - x;
        for (var i = 0; i < bmp.length; i++) {
            var _y, _x = i % w + x, _p = bmp[i];

            for (var j = 0; j < 8; j++) {
                _y = (y + Math.floor(i / w)) * 8 + j;
                if (_y >= y0 * 8) {
                    return;
                }
                // if (_y >= 16) {
                //     this._setFillStyle('#00ffff');
                // } else {
                //     this._setFillStyle('#ffc000');
                // }

                if (_p % 2) {
                    this._fillRect(_x, _y, 1, 1);
                } else {
                    this._clearRect(_x, _y, 1, 1);
                }
                _p >>= 1;
            }
        }
        if (external) {
            this.shape.draw();
        }
    }

    printChar = (x, y, char) => {
        var charArray = ' !"#$%&\'()*+,-./0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`abcdefghijklmnopqrstuvwxyz{|}~';
        let index = charArray.indexOf(char);
        if (index === -1) {
            index = 31;
        }

        var pixel = font[index];
        this.draw(x * 8, y * 2, x * 8 + 8, y * 2 + 2, pixel, false);
    }

    print = (line, str, wrap) => {
        if (typeof (line) !== 'number') {
            str = line;
            wrap = str;
            line = 0;
        }
        this._clearRect(0, line * YY / 4, XX, (line + 1) * YY / 4);

        var c = 0;
        for (var i = 0; i < str.length; i++) {
            if (str[i] === '\n' || c >= 16 && wrap) {
                line++;
                this._clearRect(0, line * YY / 4, XX, (line + 1) * YY / 4);
                c = 0;
                i--;
            } else {
                if (c >= 16 || line >= 4) {
                    continue;
                }
                this.printChar(c++, line, str[i]);
            }
        }
        this.shape.draw();
    }

    clean = () => {
        this._clearRect(0, 0, 128, 64);
        this.shape.draw();
    }

    initDraw = (canvasContext) => {
        // let start = +new Date();
        let p = this.props.w / XX;
        if (window.Konva.SceneContext.prototype.isPrototypeOf(canvasContext)) {
            canvasContext.fillStyle = "#2d2d2d";
            canvasContext.fillRect(0, 0, XX * p, YY * p);
            if (this.props.switchOn) {
                canvasContext.fillStyle = "#ffc000";
                for (let j = 0; j < YY / 4; j++) {
                    for (let i = 0; i < XX; i++) {
                        if (this.pixels[j * XX + i]) {
                            canvasContext.fillRect(i * p, j * p, p, p);
                        }
                    }
                }
                canvasContext.fillStyle = "#00ffff";
                for (let j = YY / 4; j < YY; j++) {
                    for (let i = 0; i < XX; i++) {
                        if (this.pixels[j * XX + i]) {
                            canvasContext.fillRect(i * p, j * p, p, p);
                        }
                    }
                }
            }else {
                this.pixels.fill(false);
            }
        }
        // console.log('render end, ' + (+new Date() - start));
    }

    componentDidMount() {
        window.mj2 = this.shape;
    }

    render() {
        return (
            <Shape ref={el => { this.shape = el; }} sceneFunc={this.initDraw} x={this.props.x} y={this.props.y} width={this.props.w} height={this.props.h} />
        );
    }
}

export default OLED;